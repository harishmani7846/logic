import streamlit as st
import pandas as pd
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score

# Commented out IPython magic to ensure Python compatibility.
# %pip install streamlit

st.title("ğŸ©º Diabetes Prediction using Logistic Regression")

# âœ… Load local file (must exist in GitHub repo)
df = pd.read_excel("/content/diabetes.xlsx")

df.head()

st.subheader("ğŸ“Š Dataset Preview")
st.write(df.head())

X = df[['age', 'plas', 'insu', 'mass']]
y = df['class']

# Step 4: Train model
model = LogisticRegression()
model.fit(X, y)

st.subheader("ğŸ‘¤ Enter Patient Information")
age = st.number_input("Age", 1, 120, 30)
plas = st.number_input("Plasma Glucose", 0, 200, 100)
insu = st.number_input("Insulin", 0, 1000, 100)
mass = st.number_input("BMI", 10.0, 80.0, 25.0)

if st.button("ğŸ” Predict"):
    prediction = model.predict([[age, plas, insu, mass]])
    if prediction[0] == 1:
        st.success("ğŸ¯ Prediction: Diabetic")
    else:
        st.success("ğŸ’ª Prediction: Not Diabetic")

st.subheader("ğŸ“ˆ Model Training Accuracy")
acc = accuracy_score(y, model.predict(X))
st.write(f"Accuracy: **{acc:.2f}**")